#include "FxParser.h"

//-------------------------------------------------------------------------------------------------
//      メインエントリーポイントです.
//-------------------------------------------------------------------------------------------------
int main(int argc, char** argv)
{
    if (argc <= 1)
    { return 0; }

    FxParser parser;
    parser.Parse(argv[1]);

    if (argc >= 2)
    { parser.WriteVariationInfo(argv[2]); }

    if (argc >= 3)
    { parser.WriteSourceCode(argv[3]); }

    return 0;
}